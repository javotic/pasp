package mx.gob.edomex.microservicios.serviciosreportes.models;

/**
 *
 * @author smartinez
 */
public class EscalafonEscolaridadDTO {
    private String gradoEscolaridad;
    private String nivelEscolaridad;
    private String escolaridad;
    private String puntosEscolaridad;
    private String validado;
    private String idNivelEscolaridad;

    public String getGradoEscolaridad() {
        return gradoEscolaridad;
    }

    public void setGradoEscolaridad(String gradoEscolaridad) {
        this.gradoEscolaridad = gradoEscolaridad;
    }

    public String getNivelEscolaridad() {
        return nivelEscolaridad;
    }

    public void setNivelEscolaridad(String nivelEscolaridad) {
        this.nivelEscolaridad = nivelEscolaridad;
    }

    public String getEscolaridad() {
        return escolaridad;
    }

    public void setEscolaridad(String escolaridad) {
        this.escolaridad = escolaridad;
    }

    public String getPuntosEscolaridad() {
        return puntosEscolaridad;
    }

    public void setPuntosEscolaridad(String puntosEscolaridad) {
        this.puntosEscolaridad = puntosEscolaridad;
    }

    public String getValidado() {
        return validado;
    }

    public void setValidado(String validado) {
        this.validado = validado;
    }

    public String getIdNivelEscolaridad() {
        return idNivelEscolaridad;
    }

    public void setIdNivelEscolaridad(String idNivelEscolaridad) {
        this.idNivelEscolaridad = idNivelEscolaridad;
    }

}
